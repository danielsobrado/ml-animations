function matchesLesson(item, animation) {
  return item.lessonIds?.includes(animation.id) || item.categoryIds?.includes(animation.categoryId);
}

export function getCurriculumDepth(animation) {
  return {
    comparisons: conceptComparisons.filter((item) => matchesLesson(item, animation)),
    failures: failureGalleryItems.filter((item) => matchesLesson(item, animation)),
    paperSignals: paperReadingSignals.filter((item) => matchesLesson(item, animation)),
    caveats: caveatBoxes.filter((item) => matchesLesson(item, animation)),
  };
}
