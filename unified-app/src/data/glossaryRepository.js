function symmetrizeConfusedWith(terms) {
  const peerSets = new Map(terms.map((term) => [term.id, new Set(term.confusedWith || [])]));

  for (const [id, peers] of peerSets) {
    for (const peerId of peers) {
      if (!peerSets.has(peerId)) continue;
      peerSets.get(peerId).add(id);
    }
  }

  return terms.map((term) => ({
    ...term,
    confusedWith: [...peerSets.get(term.id)].filter((peerId) => peerId !== term.id).sort(),
  }));
}

