export function getGlossaryTermsGroupedByCategory() {
  const groups = new Map();

  for (const term of glossaryTerms) {
    if (!groups.has(term.category)) {
      groups.set(term.category, []);
    }
    groups.get(term.category).push(term);
  }

  return [...groups.entries()]
    .map(([category, terms]) => ({
      category,
      terms: terms.sort((left, right) => left.term.localeCompare(right.term)),
    }))
    .sort((left, right) => left.category.localeCompare(right.category));
}

export function getGlossaryTerm(idOrTerm) {
  if (!idOrTerm) return undefined;
  const normalized = slugify(idOrTerm);
  const alias = String(idOrTerm).toLowerCase();
  return glossaryById.get(normalized) || glossaryByTerm.get(alias) || glossaryByAlias.get(alias);
}

export function getGlossaryTerms(ids) {
  return ids.map((id) => getGlossaryTerm(id)).filter(Boolean);
}

export function getGlossaryTermsForCategory(categoryId) {
  return getGlossaryTerms(GLOSSARY_IDS_BY_CATEGORY[categoryId] || GLOSSARY_IDS_BY_CATEGORY['math-fundamentals']);
}
